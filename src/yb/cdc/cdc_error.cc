#include "yb/cdc/cdc_error.h"

#include "yb/common/wire_protocol.h"

namespace yb::cdc {

void SetupError(CDCErrorPB* error, const Status& status) {
  StatusToPB(status, error->mutable_status());
  auto code = CDCError::ValueFromStatus(status);
  if (code) {
    error->set_code(*code);
  }
}

} // namespace yb::cdc
