inline std::string ToString(YBPgErrorCode code) {
  std::string result;
  result.resize(5);
  uint32_t u32_code = static_cast<uint32_t>(code);
  for (int i = 0; i < 5; ++i) {
    result[i] = '0' + ((u32_code >> (i * 6)) & 0x3f);
  }
  return result;
}

